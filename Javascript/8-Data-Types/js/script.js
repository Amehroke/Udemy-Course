//String 
let string_demo = "This is a string";
console.log('string: ', string_demo);   

//Number
let number_demo = 10;
console.log('number: ', number_demo);

//Float
let float_demo = 10.5;
console.log('float: ', float_demo);

//Boolean
let boolean_demo = true;
console.log('boolean: ', boolean_demo);

//Null
let null_demo = null;
console.log('Null: ', null_demo);

//Undefined
let undefined_demo = undefined;
console.log('undefined: ', undefined_demo);
//or 

let undefined_demo_2;
console.log('undefined: ', undefined_demo_2);

//object
const object_demo = {
    name: "John",
    age: 25,
    isStudent: false
};
console.log('objects: ', object_demo);


//Array
const array_demo = [1, 2, 'mango', 4, 5];
console.log('Array: ', array_demo);


//Another way to see what data type a variable is we can use the typeof operator    

